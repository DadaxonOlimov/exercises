#  Agar sana oyning mumkin bo'lgan qiymatiga mos kelmasa, unda bu holda siz "Error" ni chiqarishingiz kerak.
a =int(input())
if 2<a<6:
    print('Spring') 
elif 5<a<9:
    print('Summer')
elif 8<a<12:
    print('Autumn') 
elif a < 3 or a == 12:
    print("Winter")
else:
    print('Error')        
 
  
   
    
     
      
       
        
         

          


                            








                            